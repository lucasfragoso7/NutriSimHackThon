package NutriSim.Domain.FoodProperties;

public class Energy {
	 private double kcal;
	 private double kj;
	public double getKcal() {
		return kcal;
	}
	public void setKcal(double kcal) {
		this.kcal = kcal;
	}
	public double getKj() {
		return kj;
	}
	public void setKj(double kj) {
		this.kj = kj;
	}

}
